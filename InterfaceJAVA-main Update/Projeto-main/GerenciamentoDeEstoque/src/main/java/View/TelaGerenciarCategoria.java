    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel salvar = (DefaultTableModel) tableCategoria.getModel();
        
        try{
            Connection con = Conexao.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("selecione a categoria ");
            while(rs.next()){
                salvar.addRow(new Object[]{rs.getString("category_pk"),rs.getString("nome")});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        // salvar o nome na tabela
        btnAtualizar.setEnabled(false);
        //chamar o botão atualizar
    }//GEN-LAST:event_formComponentShown

    private void tableCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCategoriaMouseClicked
        // TODO add your handling code here:
        
        int index = tableCategoria.getSelectedRow();
        TableModel model = tableCategoria.getModel();
        String id = model.getValueAt(index, 0).toString();
        categorypk = Integer.parseInt(id);
        //clicar na tabela
        
        String nome = model.getValueAt(index, 1).toString();
        txtNomeCategoria.setText(nome);
        btnSalvar.setEnabled(false);
        btnAtualizar.setEnabled(true);
        //fazer o botão de alterar modificar no mysql ou algo assim
    }//GEN-LAST:event_tableCategoriaMouseClicked

