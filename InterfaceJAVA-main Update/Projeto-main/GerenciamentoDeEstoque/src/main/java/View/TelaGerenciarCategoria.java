    //BOTAO ATUALIZAR UM ITEN NA TABELA CATEGORIA
    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        String nome = txtNomeCategoria.getText();
        if(validateFields()){
            JOptionPane.showMessageDialog(null, "Preencha Todos os campos ");
        }else{
            try{
                Connection con = Conexao.getCon();
                PreparedStatement ps = con.prepareStatement("Atualizar a categoria nome=? na category_pk");
                ps.setString(1, nome);
                ps.setInt(2, categorypk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Categoria Atualizada");
                setVisible(false);
                new TelaGerenciarCategoria().setVisible(true);
            }catch(Exception e ){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

