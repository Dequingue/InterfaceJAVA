    private void getAllCategory(){
        try{
            Connection con = Conexao.getCon();
            Statement st =  con.createStatement();
            ResultSet rs =st.executeQuery("Selecione as categorias");
            ComboBoxProduto.removeAllItems();
            while(rs.next()){
                ComboBoxProduto.addItem(rs.getString("Categorias")+"-"+rs.getString("nome"));
                //teste categoria
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private boolean validateFields(String formType){
        if(formType.equals("edit")&& !txtNomeProduto.getText().equals("")&& !txtPrecoProduto.getText().equals("")&& !txtDescricaoProduto.getText().equals("")){
            return false;
        }    
        else if(formType.equals("new")&& !txtNomeProduto.getText().equals("")&& !txtQuantidadeProduto.getText().equals("")&& !txtPrecoProduto.getText().equals("")&& !txtDescricaoProduto.getText().equals("")){
            return false;
        }else{
            return true;
        }     
            
        
    }
