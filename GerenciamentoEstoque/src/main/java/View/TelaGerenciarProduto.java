    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        getAllCategory();
        DefaultTableModel model = (DefaultTableModel) tableProdutos.getModel();
        try{
            Connection con = Conexao.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("selecione os produtos da categoria!");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("produto_pk"),rs.getString("nome"),rs.getString("quantidade"),rs.getString("preço"),rs.getString("descrição"),rs.getString("categoria"),rs.getString(0)});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        btnAtualizarProduto.setEnabled(false);
        
    }//GEN-LAST:event_formComponentShown

    private void btnSalvarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarProdutoActionPerformed
        // TODO add your handling code here:
        String nome = txtNomeProduto.getText();
        String quantidade = txtQuantidadeProduto.getText();
        String preco = txtPrecoProduto.getText();
        String descricao = txtDescricaoProduto.getText();
        String categoria = (String) ComboBoxProduto.getSelectedItem();
        String categoriaId[] = categoria.split("-",0);
        if(validateFields("new")){
            JOptionPane.showMessageDialog(null, "todos os arquivos requeridos");
        }
        else{
            try{
                Connection con = Conexao.getCon();
                PreparedStatement ps = con.prepareStatement("insira os produtos(nome, quantidade,preço,categoria,descrição)");
                ps.setString(1,nome);
                ps.setString(2,preco);
                ps.setString(3,quantidade);
                ps.setString(4,descricao);
                ps.setString(5,categoriaId[0]);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Produto adicionado com sucesso");
                setVisible(false);
                new TelaGerenciarProduto().setVisible(true);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSalvarProdutoActionPerformed

    private void tableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProdutosMouseClicked
        // TODO add your handling code here:
        int index = tableProdutos.getSelectedRow();
        TableModel model = tableProdutos.getModel();
        String id = model.getValueAt(index,0).toString();
        product_pk = Integer.parseInt(id);
        
        String nome = model.getValueAt(index, 1).toString();
        txtNomeProduto.setText(nome);
        
        String quantidade = model.getValueAt(index, 2).toString();
        txtQuantidadeProduto.setText("adicionar quantidade");
        totalQuantidade = Integer.parseInt(quantidade);
        
        String preco = model.getValueAt(index, 3).toString();
        txtPrecoProduto.setText(preco);
        
        String descricao = model.getValueAt(index, 4).toString();
        txtDescricaoProduto.setText(descricao);
        
        ComboBoxProduto.removeAllItems();
        String categoriaId = model.getValueAt(index, 5).toString();
                
        String categoriaNome = model.getValueAt(index, 6).toString();
        ComboBoxProduto.addItem(categoriaId+"-"+categoriaNome);
        
                try{
            Connection con = Conexao.getCon();
            Statement st =  con.createStatement();
            ResultSet rs =st.executeQuery("Selecione as categorias");
            
            while(rs.next()){
                if(Integer.parseInt(categoriaId) != rs.getInt(1)){
                    ComboBoxProduto.addItem(rs.getString("Categorias")+"-"+rs.getString("nome"));
                }
                //teste categoria
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        btnSalvarProduto.setEnabled(true);
    }//GEN-LAST:event_tableProdutosMouseClicked

    private void btnAtualizarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarProdutoActionPerformed
        // TODO add your handling code here:
         String nome = txtNomeProduto.getText();
        String quantidade = txtQuantidadeProduto.getText();
        String preco = txtPrecoProduto.getText();
        String descricao = txtDescricaoProduto.getText();
        String categoria = (String) ComboBoxProduto.getSelectedItem();
        String categoriaId[] = categoria.split("-",0);
        if(validateFields("edit")){
            JOptionPane.showMessageDialog(null, "todos os arquivos requeridos");
        }
        else{
            try{
                if(!quantidade.equals("")){
                    totalQuantidade= totalQuantidade+Integer.parseInt(quantidade);
                }
                Connection con = Conexao.getCon();
                PreparedStatement ps = con.prepareStatement("produtos salvos!");
                ps.setString(1,nome);
                ps.setString(2,preco);
                ps.setInt(2,totalQuantidade);
                ps.setString(4,descricao);
                ps.setString(5,categoriaId[0]);
                ps.setInt(6, product_pk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Produtos atualizados com sucesso");
                setVisible(false);
                new TelaGerenciarProduto().setVisible(true);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
    }//GEN-LAST:event_btnAtualizarProdutoActionPerformed

